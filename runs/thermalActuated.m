clear; close all;

jobManager = JobManager();

opt.maxtime = 15*60;
opt.verbose = 1;
opt.ftol_rel = 1e-7;
%opt.xtol_abs = 1e-7*ones(size(fem.mainDensities));
opt.algorithm = NLOPT_LD_MMA;

timeSteps = 50;
volumeFraction = 0.2;
radius = 0.002;
P = 1;
k = 5e5;
tFinal = 600;

material_1 = Material(1, 1.767e6, 1e-3*eye(3), 3e2, 0.45, 0);
material_2 = Material(1e3, 1.767e3, 0.22*eye(3), 1.1e9, 0.45, 8e-5);
materials = [material_1, material_2];
%%
width = 0.1;
height = 0.1;
mesh = StructuredMesh([31, width], [31, height]);
globalCoord = mesh.coordinates();


topAndLeftNodes = find(globalCoord(1, :) == 0 | ...
    globalCoord(2, :) == height);
topRightNode = find(globalCoord(1, :) == height & globalCoord(2, :) == width);
topCornerExpanded = find(globalCoord(2, :) == height & ...
    globalCoord(1, :) <= width/25);
bottomNodes = find(globalCoord(2, :) == 0);
rightCornerExpanded = find((globalCoord(1, :) >= width*9/10 & globalCoord(2, :) == 0) | ...
                           (globalCoord(2, :) <= height/10 & globalCoord(1, :) == width));
rightCorner = find(globalCoord(1, :) >= width*9/10 & globalCoord(2, :) == 0);

% Create boundary conditions
fixed = struct(...
    'nodes', rightCornerExpanded, ...
    'type', 'Dirichlet', ...
    'value', 0, ...
    'components', [1, 1], ...
    'timeSteps', 1:timeSteps ...
);

symmetry = struct(...
    'nodes', bottomNodes, ...
    'type', 'Dirichlet', ...
    'value', 0, ...
    'components', [0, 1], ...
    'timeSteps', 1:timeSteps ...
);

output = struct( ...
    'nodes', topRightNode, ...
    'type', 'dummy', ...
    'name', 'josse', ...
    'value', 1e4, ...
    'components', [1, 0], ...
    'timeSteps', timeSteps ...
);

spring = struct( ...
    'nodes', topRightNode, ...
    'type', 'Robin', ...
    'value', k, ...
    'components', [1, 0], ...
    'timeSteps', 1:timeSteps ...
);

tempPrescribed = struct( ...
    'nodes', bottomNodes, ...
    'type', 'Dirichlet', ...
    'value', 100, ...
    'timeSteps', 1:timeSteps ...
);


% Create body conditions
body = struct(...
    'type', 'main' ...
);

mechFEM = OptMechFEMStructured(numel(materials), mesh, timeSteps, "plane stress");

mechFEM.addBoundaryCondition(fixed);
mechFEM.addBoundaryCondition(symmetry);
mechFEM.addBoundaryCondition(output);
mechFEM.addBoundaryCondition(spring);
mechFEM.addBodyCondition(body);

mechFEM.setMaterial(material_2);
[E, EDer, alpha, alphaDer] = MechSIMP(materials, 3, 1);
mechFEM.addInterpFuncs(E, EDer, alpha, alphaDer);

heatFEM = OptThermoMechStructured(mechFEM, numel(materials), mesh, tFinal, timeSteps, 1);

heatFEM.addBoundaryCondition(tempPrescribed);
heatFEM.addBodyCondition(body);

heatFEM.setMaterial(material_2);

[kappaF, kappaFDer, cp, cpDer] = HeatSIMP(materials, 1, 1);
heatFEM.addInterpFuncs(kappaF, kappaFDer, cp, cpDer);

options = struct(...
    'heavisideFilter', false, ...
    'designFilter', true, ...
    'filterRadius', radius, ...
    'filterWeightFunction', @(dx, dy, dz) max(radius-sqrt(dx.^2+dy.^2+dz.^2), 0), ...
    'materials', materials, ...
    'plot', false ...
);

%%
coupledFEM = heatFEM;
massLimit = volumeFraction * sum(heatFEM.volumes * material_2.density);
topOpt = ThermallyActuatedProblem(coupledFEM, options, massLimit);
initial = volumeFraction*ones(size(heatFEM.designPar));

job = Job(topOpt, initial, opt);
jobManager.add(job);
%%
jobManager.runAll();
%%
jobManager.plotAll();
%%
saveAnswer = questdlg("Would you like to save all jobs?", "Yes", "No");
switch saveAnswer
    case "Yes"
        jobManager.saveAll();
    case "No"
        disp("Did not save the jobs");
end